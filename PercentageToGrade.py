# Grade Calculator in python

Percentage=float(input("Ente your Precentage :- "))

if(Percentage>=81 and Percentage <=100):
    print("Very Good")
elif(Percentage>=61 and Percentage <=80):
    print("Good")    
elif(Percentage>=41 and Percentage <=60):
    print("Average")    
else:
    print("Bad")    
    